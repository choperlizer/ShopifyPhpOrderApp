    $.ajax({
      type : 'POST',
      url : Url,
      data : FormData,
      dataType:"json",
      beforeSend: function(data){
       
        },
      success: function(data){
        if(data['code'] == 200)
        {
          $('.mss').html('Setting updated');
          
        }
        else
        {
            $('.mss').html('some thing went wrong');
        }
      },async: false
    });
  }
